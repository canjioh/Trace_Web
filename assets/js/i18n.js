let LANG = (localStorage.getItem('trace-lang') || (navigator.language || '').slice(0, 2)) === 'en' ? 'en' : 'it';

function t(key, ...args) {
  let s = (I18N[LANG] && I18N[LANG][key]) || I18N.it[key] || key;
  args.forEach((a, i) => { s = s.split(`{${i}}`).join(a); });
  return s;
}

function reactionNote(id) {
  return (REACTION_NOTES[LANG] || REACTION_NOTES.it)[id] || '';
}

function applyI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-html]').forEach((el) => { el.innerHTML = t(el.dataset.i18nHtml); });
  document.documentElement.lang = LANG;
}

function setLang(l) {
  LANG = l;
  localStorage.setItem('trace-lang', l);
}
